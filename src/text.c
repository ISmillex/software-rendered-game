bool glyph_cache_init(GlyphCache *cache, const char *font_bitmap_path,
                       int glyph_w, int glyph_h) {
    Texture *tex = NULL;
    if (font_bitmap_path) {
        FILE *test = fopen(font_bitmap_path, "rb");
        if (test) {
            fclose(test);
            tex = texture_load_bmp(font_bitmap_path);
        }
    }
    if (!tex) {
        return glyph_cache_init_generated(cache, glyph_w, glyph_h);
    }

    cache->glyph_width  = glyph_w;
    cache->glyph_height = glyph_h;
    cache->atlas        = tex->pixels;
    cache->atlas_width  = tex->width;
    cache->atlas_height = tex->height;
    free(tex); // free the Texture struct but keep pixels

    for (int i = 32; i < 128; i++) {
        int index = i - 32;
        int col = index % 16;
        int row = index / 16;
        cache->glyphs[i].x = col * glyph_w;
        cache->glyphs[i].y = row * glyph_h;
    }

    for (int i = 0; i < 32; i++) {
        cache->glyphs[i].x = 0;
        cache->glyphs[i].y = 0;
    }

    return true;
}

bool glyph_cache_init_generated(GlyphCache *cache, int glyph_w, int glyph_h) {
    (void)glyph_w;
    cache->glyph_width  = 8;
    cache->glyph_height = 16;
    cache->atlas_width  = 16 * 8;  // 128
    cache->atlas_height = 6 * 16;  // 96

    int total_pixels = cache->atlas_width * cache->atlas_height;
    cache->atlas = calloc(total_pixels, sizeof(uint32_t));
    if (!cache->atlas) return false;

    // Render built-in font into atlas
    for (int i = 32; i < 128; i++) {
        int index = i - 32;
        int col = index % 16;
        int row = index / 16;
        int ox = col * 8;
        int oy = row * 16;

        cache->glyphs[i].x = ox;
        cache->glyphs[i].y = oy;

        for (int py = 0; py < 16; py++) {
            unsigned char byte = builtin_font[index][py];
            for (int px = 0; px < 8; px++) {
                if (byte & (0x80 >> px)) {
                    cache->atlas[(oy + py) * cache->atlas_width + (ox + px)] = 0xFFFFFFFF;
                }
            }
        }
    }

    for (int i = 0; i < 32; i++) {
        cache->glyphs[i].x = 0;
        cache->glyphs[i].y = 0;
    }

    (void)glyph_h;
    return true;
}

void text_draw_char(const GlyphCache *cache, char c, int dest_x, int dest_y,
                    uint32_t color) {
    unsigned char uc = (unsigned char)c;
    if (uc >= 128) return;

    GlyphEntry g = cache->glyphs[uc];

    for (int row = 0; row < cache->glyph_height; row++) {
        int sy = dest_y + row;
        if (sy < 0 || sy >= WINDOW_HEIGHT) continue;

        for (int col = 0; col < cache->glyph_width; col++) {
            int sx = dest_x + col;
            if (sx < 0 || sx >= WINDOW_WIDTH) continue;

            uint32_t texel = cache->atlas[(g.y + row) * cache->atlas_width + (g.x + col)];

            if (texel & 0x00FFFFFF) {
                display_buffer[sy * WINDOW_WIDTH + sx] = color;
            }
        }
    }
}

void text_draw_string(const GlyphCache *cache, const char *str,
                      int x, int y, uint32_t color) {
    int cursor_x = x;
    for (int i = 0; str[i] != '\0'; i++) {
        if (str[i] == '\n') {
            cursor_x = x;
            y += cache->glyph_height;
            continue;
        }
        text_draw_char(cache, str[i], cursor_x, y, color);
        cursor_x += cache->glyph_width;
    }
}

int text_string_width(const GlyphCache *cache, const char *str) {
    int max_width = 0;
    int width = 0;
    for (int i = 0; str[i] != '\0'; i++) {
        if (str[i] == '\n') {
            if (width > max_width) max_width = width;
            width = 0;
        } else {
            width += cache->glyph_width;
        }
    }
    return width > max_width ? width : max_width;
}
